int		ft_count_if(char **tab, int (*f)(char *))
{
	int		x;
	int		i;

	x = 0;
	i = 0;
	while (tab[x] != 0)
	{
		if (f(tab[x]) == 1)
		{
			i++;
		}
		x++;
	}
	return (i);
}
